
public class GameMode {
	protected String name;
	protected int rows;
	protected int columns;
	protected int bombQuantity;
	
	public String getModeName(){
		return name;
	}
	
	public int getRows(){
		return rows;
	}
	
	public int getColumns(){
		return columns;
	}
	
	public int getBombQuantity(){
		return bombQuantity;
	}
}
